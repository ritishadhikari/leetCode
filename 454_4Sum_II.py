from typing import List

class Solution:
    def fourSumCount(self, nums1: List[int], 
                     nums2: List[int], 
                     nums3: List[int], 
                     nums4: List[int]) -> int:
        
        trace={}
        for n1 in nums1:
            for n2 in nums2:
                if n1+n2 not in trace:
                    trace[n1+n2]=1
                else:
                    trace[n1+n2]+=1
        count=0
        for n3 in nums3:
            for n4 in nums4:
                valueToSubtract=-(n3+n4)
                if valueToSubtract in trace:
                    count+=trace[valueToSubtract]
        return count


